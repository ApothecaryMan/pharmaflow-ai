// Translation cache for better performance
const translationCache = new Map<string, string>();

// Helper function to get translated text with caching
export const getMenuTranslation = (text: string, language: 'EN' | 'AR'): string => {
  // For English, return as-is (no translation needed)
  if (language === 'EN') return text;
  
  // Check cache first
  const cacheKey = `${language}:${text}`;
  if (translationCache.has(cacheKey)) {
    return translationCache.get(cacheKey)!;
  }
  
  // Look up translation
  const translation = MENU_TRANSLATIONS.AR[text] || text;
  
  // Cache the result
  translationCache.set(cacheKey, translation);
  
  return translation;
};
